// Mappa per accesso rapido per ID
export const ABILITY_MAP = new Map(ABILITIES.map(a => [a.id, a]));

// Abilità filtrate per tipo
export function getAbilitiesByType(type: AbilityType): Ability[] {
  return ABILITIES.filter(a => a.type === type);
}

// Abilità disponibili per una classe specifica
export function getAbilitiesForClass(heroClass: HeroClass): Ability[] {
  return ABILITIES.filter(a => a.heroClass === null || a.heroClass === heroClass);
}

// Abilità disponibili per una classe e rarità
export function getAvailableAbilities(heroClass: HeroClass, rarity: Rarity): Ability[] {
  const { RARITY_ORDER } = require('../types');
  const rarityIndex = RARITY_ORDER.indexOf(rarity);

  return ABILITIES.filter(a => {
    // Deve essere della classe giusta o generica
    if (a.heroClass !== null && a.heroClass !== heroClass) return false;
    // Verifica rarità minima
    if (a.minRarity) {
      const minIndex = RARITY_ORDER.indexOf(a.minRarity);
      if (rarityIndex < minIndex) return false;
    }
    return true;
  });
}
